% simple (straight forward) arithmetic

% npos(X) -> + X.

% nneg(X) -> - X.

nadd(X, Y) -> X + Y.

nsub(X, Y) -> (X - Y).

nmul(X, Y) -> (X * Y).

ndiv(X, Y) -> X / Y.

% ibnot(X) -> bnot X.

idiv(X, Y) -> X div Y.

irem(X, Y) -> X rem Y.

iband(X,Y) -> X band Y.

ibor(X,Y) -> X bor Y.

ibxor(X,Y) -> X bxor Y.

ibsl(X,Y) -> X bsl Y.

ibsr(X,Y) -> X bsr Y.






